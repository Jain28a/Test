class Employee 
{    
    int empId;    
    String empName;   
    int departmentId;

    Employee(int empId, String empName, int departmentId)
    {
        this.empId = empId;
        this.empName = empName;
        this.departmentId = departmentId;
    }
    Employee(int empId, String empName)
    {
        this.empId = empId;
        this.empName = empName;
    }
}  

